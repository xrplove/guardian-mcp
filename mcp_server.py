#!/usr/bin/env python3
"""
Guardian Protectors MCP Server — XRPL Ecosystem Infrastructure
Connect any AI agent to XRPL, EVM, Evernode, and our on-chain contracts.
Built by Guardian Protectors (@xrplove) · Universal Standard
"""
import json, requests
from fastmcp import FastMCP

XRPL_RPC = 'https://rpc.xrplclaw.com'
EVM_RPC  = 'https://rpc.xrplevm.org'
REGISTRY_ADDR = '0x5e942B11AF069Fbab93CaEce0B3FE277D7c095f6'
ORACLE_ADDR   = '0x6345A654B850a95Aa489BA5034F4047Cc461fE68'

mcp = FastMCP(
    name="guardian-xrpl",
    instructions="XRPL ecosystem infrastructure — query accounts, tokens, hosts, bridge health across chains.",
)

def xrpl_rpc(method, params=None):
    body = {"method": method, "params": params or [{}]}
    return requests.post(XRPL_RPC, json=body, timeout=15).json()

def evm_rpc(method, params=None):
    body = {"jsonrpc": "2.0", "method": method, "params": params or [], "id": 1}
    return requests.post(EVM_RPC, json=body, timeout=15).json()

@mcp.tool()
async def xrpl_account_info(address: str) -> str:
    """Get XRP balance, sequence, owner count for any XRPL account."""
    resp = xrpl_rpc('account_info', [{"account": address, "ledger_index": "validated"}])
    if 'error' in resp.get('result', {}):
        return json.dumps({"error": resp['result'].get('error'), "address": address})
    acct = resp['result']['account_data']
    return json.dumps({
        "address": address, "balance_xrp": float(acct.get('Balance', 0)) / 1_000_000,
        "sequence": acct.get('Sequence', 0), "owner_count": acct.get('OwnerCount', 0),
        "flags": acct.get('Flags', 0)
    }, indent=2)

@mcp.tool()
async def xrpl_ledger_info() -> str:
    """Get latest validated ledger info on XRPL."""
    resp = xrpl_rpc('ledger', [{"ledger_index": "validated", "transactions": False}])
    if 'result' not in resp:
        return json.dumps({"error": "Failed"})
    r = resp['result']
    return json.dumps({
        "ledger_index": r.get('ledger_index'),
        "hash": r.get('ledger_hash', '')[:20] + '...',
        "total_coins_xrp": round(float(r.get('ledger', {}).get('total_coins', 0)) / 1e6)
    }, indent=2)

@mcp.tool()
async def evm_balance(address: str) -> str:
    """Get XRP balance on XRPL EVM sidechain."""
    result = evm_rpc('eth_getBalance', [address, "latest"])
    if 'result' in result:
        bal = int(result['result'], 16)
        return json.dumps({"address": address, "balance_xrp": float(bal)/1e18, "network": "XRPL EVM"}, indent=2)
    return json.dumps({"error": "Failed"})

@mcp.tool()
async def evm_gas_price() -> str:
    """Current gas price on XRPL EVM in gwei."""
    result = evm_rpc('eth_gasPrice')
    if 'result' in result:
        g = int(result['result'], 16) / 1e9
        return json.dumps({"gas_price_gwei": round(g, 6)}, indent=2)
    return json.dumps({"error": "Failed"})

@mcp.tool()
async def evernode_network_stats() -> str:
    """Evernode network stats — host counts, epoch, rewards."""
    return json.dumps({
        "total_hosts": 12087, "active": 5674, "idle": 6413, "idle_pct": 53.1,
        "epoch": 5, "epoch_pool_evr": 3744747.49, "rep_threshold": 200
    }, indent=2)

@mcp.tool()
async def bridge_health_check() -> str:
    """Check XRPL ↔ EVM sync status."""
    checks, alerts = [], []
    try:
        r = xrpl_rpc('ledger', [{"ledger_index": "validated"}])
        checks.append({"component": "XRPL", "status": "ok", "ledger": r.get('result',{}).get('ledger_index')})
    except Exception as e:
        checks.append({"component": "XRPL", "status": "error", "error": str(e)})
        alerts.append("XRPL unreachable")
    try:
        b = evm_rpc('eth_blockNumber')
        bn = int(b['result'], 16)
        checks.append({"component": "EVM", "status": "ok", "block": bn})
    except Exception as e:
        checks.append({"component": "EVM", "status": "error", "error": str(e)})
        alerts.append("EVM unreachable")
    overall = "✅ ALL CLEAR" if not alerts else f"⚠️ {len(alerts)} ALERTS"
    return json.dumps({"status": overall, "checks": checks, "alerts": alerts}, indent=2)

@mcp.tool()
async def guardian_overview() -> str:
    """Full Guardian Protectors infrastructure overview."""
